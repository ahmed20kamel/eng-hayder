// اضبط اتجاه الصفحة حسب اللغة
export function applyDir(lang) {
  const isRTL = lang === "ar";
  document.documentElement.lang = lang;
  document.documentElement.dir = isRTL ? "rtl" : "ltr";
}
applyDir(i18n.language);

i18n.on("languageChanged", applyDir);

